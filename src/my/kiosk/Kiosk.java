    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
       txtReceipt.append("\n \n Total Bill:  " + total);
        
        try {
            MessageFormat header = new MessageFormat("Mcdolibee");
            MessageFormat footer = new MessageFormat("Mcdolibee");
            
            boolean complete = txtReceipt.print(header, footer);
            if(complete){
                JOptionPane.showMessageDialog(null, "Done!", 
                        
                        "Information", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Printing", 
                        "Printer", JOptionPane.INFORMATION_MESSAGE);
                
            } 
        } catch (PrinterException e){
            JOptionPane.showMessageDialog(null, e);
        }
      
    }//GEN-LAST:event_btnDoneActionPerformed

