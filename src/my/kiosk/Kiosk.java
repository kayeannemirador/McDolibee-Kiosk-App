    void clicked(int x, int y, JButton button){
        
        SpinnerNumberModel sModel = new SpinnerNumberModel(1, 1, 30, 1);
        JSpinner spinner = new JSpinner(sModel);
        int qty = 0; 
        
      
        if (button.getModel().isPressed()==true){     
        
    } else {
        JOptionPane.showMessageDialog(null, spinner, 
                meals[x][y].getName() + " ₱" + meals[x][y].getPrice(), 
                JOptionPane.INFORMATION_MESSAGE, button.getIcon());
       
        
        qty = (Integer)spinner.getValue();
        txtReceipt.append("\n" +qty+ " " + meals[x][y].getName()+"......................"+meals[x][y].getPrice()*qty);
        price = meals[x][y].getPrice()*qty;
        total += price;   
        }
       
    }
       
 
  
